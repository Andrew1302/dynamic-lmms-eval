"""Dynamic Graph QA task utilities for lmms-eval.

Default evaluation covers 11 graph-theory tasks generated by the dynamic-dataset package:
  node_count, edge_count, node_degree, cycle_check, edge_existence,
  connected_nodes, disconnected_nodes, reachability,
  shortest_path, triangle_counting, maximum_flow

The remaining tasks (connectivity_check, connected_components, mst) are supported
by the scoring logic below but excluded from the default dataset generation.

Answer normalization is task-aware:
  - Yes/No tasks          → lowercase + strip
  - Comma-list tasks      → split, strip items, sort, rejoin
  - Integer/number tasks  → strip and compare as strings
"""

from __future__ import annotations

from collections import defaultdict

from loguru import logger as eval_logger

_YESNO_TASKS = {"cycle_check", "edge_existence", "reachability", "connectivity_check"}
_LIST_TASKS = {"connected_nodes", "disconnected_nodes"}


def _normalize(prediction: str, task: str) -> str:
    pred = prediction.strip()
    if task in _YESNO_TASKS:
        return pred.lower()
    if task in _LIST_TASKS:
        items = [item.strip() for item in pred.split(",")]
        items = [item for item in items if item]
        return ", ".join(sorted(items, key=lambda x: int(x) if x.lstrip("-").isdigit() else x))
    # Integer / float tasks: strip whitespace only
    return pred.strip()


def dynamic_graph_qa_doc_to_visual(doc):
    """Extract visual content. Used by simple (legacy) models."""
    if doc.get("image") is not None:
        return [doc["image"].convert("RGB")]
    return []


def dynamic_graph_qa_doc_to_text(doc, lmms_eval_specific_kwargs=None):
    """Format text prompt. Used by simple (legacy) models."""
    kwargs = lmms_eval_specific_kwargs or {}
    pre = kwargs.get("pre_prompt", "")
    post = kwargs.get("post_prompt", "")
    return f"{pre}{doc['prompt']}{post}"


def dynamic_graph_qa_doc_to_messages(doc, lmms_eval_specific_kwargs=None):
    """Build ChatMessages-protocol messages. Used by chat models (recommended)."""
    visuals = dynamic_graph_qa_doc_to_visual(doc)
    text = dynamic_graph_qa_doc_to_text(doc, lmms_eval_specific_kwargs)

    content = []
    for visual in visuals:
        content.append({"type": "image", "url": visual})
    content.append({"type": "text", "text": text})

    return [{"role": "user", "content": content}]


def dynamic_graph_qa_process_results(doc, results):
    """Score a single sample.

    Returns a dict with key ``accuracy`` containing a per-sample record that
    includes the task name so ``dynamic_graph_qa_aggregate_results`` can report
    per-task breakdowns.
    """
    prediction = results[0] if results else ""
    task = doc.get("task", "unknown")
    answer = str(doc.get("answer", "")).strip()

    norm_pred = _normalize(prediction, task)
    norm_gt = _normalize(answer, task)

    score = 1.0 if norm_pred == norm_gt else 0.0
    return {"accuracy": {"task": task, "score": score}}


def dynamic_graph_qa_aggregate_results(results):
    """Aggregate per-sample scores into overall and per-task accuracy.

    Args:
        results: list of dicts returned by ``dynamic_graph_qa_process_results``
                 (each is the value of the ``accuracy`` key, i.e. ``{"task": ..., "score": ...}``).
    Returns:
        Overall accuracy as a float in [0, 1].
    """
    task_scores: dict[str, list[float]] = defaultdict(list)
    for result in results:
        task_scores[result["task"]].append(result["score"])

    total_correct = 0.0
    total_samples = 0
    for task, scores in sorted(task_scores.items()):
        task_acc = sum(scores) / len(scores)
        eval_logger.info(f"dynamic_graph_qa | {task}: {task_acc:.3f} ({int(sum(scores))}/{len(scores)})")
        total_correct += sum(scores)
        total_samples += len(scores)

    overall = total_correct / total_samples if total_samples > 0 else 0.0
    eval_logger.info(f"dynamic_graph_qa | overall: {overall:.3f} ({int(total_correct)}/{total_samples})")
    return overall
