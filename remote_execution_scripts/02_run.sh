#!/bin/bash
# =============================================================================
# 02_run.sh <job> — Launch the job in a detached tmux session on the VM.
# =============================================================================
# How it works:
#   1. Generate a launcher script locally that wraps REMOTE_RUN_CMD with
#      `exec > >(tee -a run.log) 2>&1` (redirects all child output to the log
#      *and* keeps it on stdout, so `tmux attach` still shows live output).
#   2. Copy that launcher to $REMOTE_RUNS_DIR/<job>/launch.sh on the VM.
#   3. Start a detached tmux session `lmms_<job>` running that launcher.
#   4. The launcher writes an exit-code sentinel when REMOTE_RUN_CMD returns,
#      so 04_fetch.sh can distinguish "running" from "done-ok" from "done-fail".
#
# Re-running while a session is alive is refused; use ./05_stop.sh first.
# =============================================================================

set -euo pipefail

# shellcheck disable=SC1091
source "$(dirname "$0")/lib/common.sh"
bootstrap

load_job "${1:-}"

SESSION="$(tmux_session "$JOB_NAME")"
RUN_DIR="$(remote_run_dir "$JOB_NAME")"
LOG_PATH="$(remote_log_path "$JOB_NAME")"
LAUNCHER_PATH="${RUN_DIR}/launch.sh"

if ssh_cmd "tmux has-session -t '$SESSION' 2>/dev/null"; then
    fail "tmux session '$SESSION' is already running. Use ./05_stop.sh $JOB_NAME first, or ./03_logs.sh $JOB_NAME to watch it."
fi

log "launching job '$JOB_NAME' on VM (tmux session: $SESSION)"
log "remote log: $LOG_PATH"

# Build the launcher script. REMOTE_RUN_CMD is pasted verbatim — no escaping
# needed because we use a quoted heredoc delimiter for the user command block.
LAUNCHER=$(cat <<EOF
#!/bin/bash
# Auto-generated by 02_run.sh — do not edit on the VM.
set -u
RUN_DIR='${RUN_DIR}'
LOG_PATH='${LOG_PATH}'

mkdir -p "\$RUN_DIR"
: > "\$LOG_PATH"
rm -f "\$RUN_DIR/exit_code"

# All stdout+stderr from this point (including child processes) goes to
# both the tmux pane and the log file.
exec > >(tee -a "\$LOG_PATH") 2>&1

# SSD-backed caches — the VM's home partition is too small for HF models and
# uv's download cache. These must be set BEFORE any python/uv/accelerate call.
export HF_HOME='${REMOTE_HF_HOME}'
export UV_CACHE_DIR='${REMOTE_UV_CACHE_DIR}'

cd '${REMOTE_WORKDIR}'

# Activate the venv built by `uv sync` during deploy.
if [ ! -f .venv/bin/activate ]; then
    echo "[fatal] .venv/bin/activate not found in \$(pwd) — did 01_deploy.sh run uv sync?"
    echo 1 > "\$RUN_DIR/exit_code"
    exit 1
fi
# shellcheck disable=SC1091
source .venv/bin/activate

echo "[start] \$(date -Iseconds) job=${JOB_NAME}"
echo "[cwd]   \$(pwd)"
echo "[env]   HF_HOME=\$HF_HOME UV_CACHE_DIR=\$UV_CACHE_DIR python=\$(which python)"
echo "[cmd]"
cat <<'USER_CMD_EOF'
${REMOTE_RUN_CMD}
USER_CMD_EOF
echo "--- begin output ---"

# Run the user command. Use a subshell so \$? is the user command's exit code
# (not tee's or a following builtin's).
(
${REMOTE_RUN_CMD}
)
ec=\$?

echo "--- end output ---"
echo "\$ec" > "\$RUN_DIR/exit_code"
echo "[end] \$(date -Iseconds) exit=\$ec"
EOF
)

ssh_cmd "mkdir -p '$RUN_DIR'"

# Upload the launcher (stdin → remote file).
ssh_cmd "cat > '$LAUNCHER_PATH' && chmod +x '$LAUNCHER_PATH'" <<< "$LAUNCHER"

# Start the tmux session. The command is simple — just run the launcher.
ssh_cmd "tmux new-session -d -s '$SESSION' 'bash $LAUNCHER_PATH'"

# Confirm the session came up.
if ! ssh_cmd "tmux has-session -t '$SESSION' 2>/dev/null"; then
    fail "tmux session '$SESSION' did not start. Check: ssh ${VM_USER}@${VM_HOST} tmux ls"
fi

log "${C_G}job started${C_RESET}"
echo ""
echo "  watch logs:   ./03_logs.sh $JOB_NAME"
echo "  attach tmux:  ./03_logs.sh $JOB_NAME --attach"
echo "  stop:         ./05_stop.sh $JOB_NAME"
echo "  fetch after:  ./04_fetch.sh $JOB_NAME"
